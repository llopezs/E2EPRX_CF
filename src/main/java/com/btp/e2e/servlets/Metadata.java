package com.btp.e2e.servlets;

public class Metadata {
	private String id = "";
	private String uri = "";
	private String type = "";

	public String getID() {
		return id;
	}

	public void setID(String value) {
		this.id = value;
	}

	public String getURI() {
		return uri;
	}

	public void setURI(String value) {
		this.uri = value;
	}

	public String getType() {
		return type;
	}

	public void setType(String value) {
		this.type = value;
	}
}
